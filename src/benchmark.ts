// Benchmark suite — four independent experiments:
//
//  1. Node Comparison     — four search configurations on one fixed position
//  2. Win Rate Matrix     — AI agents at different depths playing each other
//  3. Game Phase Analysis — how node count changes across early/mid/late game
//  4. Fork Detection      — agent with fork bonus vs agent without it

import { Player, ROWS, COLS, BoardState } from './types';
import { isValidMove, getNextOpenRow, dropPiece, checkWin, createEmptyBoard } from './gameLogic';
import { getBestMove } from './ai';

const tick = () => new Promise<void>(r => setTimeout(r, 0));

