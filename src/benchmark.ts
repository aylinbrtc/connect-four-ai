export interface DepthRow   { depth: number; nodes: number; timeMs: number; }
export interface VariantResult { config: string; note: string; rows: DepthRow[]; }

export const runBenchmark = async (
  board: BoardState,
  onProgress: (results: VariantResult[]) => void
): Promise<VariantResult[]> => {
  const results: VariantResult[] = [];

  const pureRows: DepthRow[] = [];
  for (let d = 1; d <= 4; d++) {
    await tick();
    const ctr = { nodes: 0 };
    const t = performance.now();
    pureMinimax(board, d, true, ctr);
    pureRows.push({ depth: d, nodes: ctr.nodes, timeMs: Math.round(performance.now() - t) });
  }
  results.push({ config: 'Pure Minimax', note: 'No pruning, no TT, left-to-right. Capped at depth 4.', rows: pureRows });
  onProgress([...results]);

  const abRows: DepthRow[] = [];
  for (let d = 1; d <= 6; d++) {
    await tick();
    const ctr = { nodes: 0 };
    const t = performance.now();
    minimaxAB(board, d, -Infinity, Infinity, true, ctr);
    abRows.push({ depth: d, nodes: ctr.nodes, timeMs: Math.round(performance.now() - t) });
  }
  results.push({ config: 'Alpha-Beta Pruning', note: 'Pruning only. Left-to-right order, no TT.', rows: abRows });
  onProgress([...results]);

  const abOrdRows: DepthRow[] = [];
  for (let d = 1; d <= 6; d++) {
    await tick();
    const ctr = { nodes: 0 };
    const t = performance.now();
    minimaxABOrder(board, d, -Infinity, Infinity, true, ctr);
    abOrdRows.push({ depth: d, nodes: ctr.nodes, timeMs: Math.round(performance.now() - t) });
  }
  results.push({ config: 'AB + Move Ordering', note: 'Center-out order [3,2,4,1,5,0,6]. No TT.', rows: abOrdRows });
  onProgress([...results]);

  const fullRows: DepthRow[] = [];
  for (let d = 1; d <= 6; d++) {
    await tick();
    const t = performance.now();
    const { nodesEvaluated, ttHitRate } = getBestMove(board, d);
    fullRows.push({ depth: d, nodes: nodesEvaluated, timeMs: Math.round(performance.now() - t) });
    void ttHitRate; // captured in Experiment 3 instead
  }
  results.push({ config: 'Full Stack (AB + TT + IDS + Ordering + Fork Det.)', note: 'Production implementation. Node count includes all IDS iterations.', rows: fullRows });
  onProgress([...results]);

  return results;
};

// -----------------------------------------------------------------------
// Experiment 2 — Win rate matrix
// -----------------------------------------------------------------------

export interface WinRateRow {
  redDepth: number;
  yellowDepth: number;
  redWins: number;
  draws: number;
  yellowWins: number;
  games: number;
}

export const runWinRateMatrix = async (
  depths: number[],
  onProgress: (rows: WinRateRow[]) => void
): Promise<WinRateRow[]> => {
  const rows: WinRateRow[] = [];

  for (const rd of depths) {
    for (const yd of depths) {
      await tick();
      // Deterministic if both depths >= 4 (no error injection) — run 1 game.
      // Non-deterministic if either depth <= 3 — run 10 games.
      const numGames = (rd <= 3 || yd <= 3) ? 10 : 1;
      let redWins = 0, draws = 0, yellowWins = 0;
      for (let g = 0; g < numGames; g++) {
        await tick();
        const outcome = simulateGame(rd, yd);
        if (outcome === 'red') redWins++;
        else if (outcome === 'yellow') yellowWins++;
        else draws++;
      }
      rows.push({ redDepth: rd, yellowDepth: yd, redWins, draws, yellowWins, games: numGames });
      onProgress([...rows]);
    }
  }

  return rows;
};

// -----------------------------------------------------------------------
// Experiment 3 — Game phase analysis (node count + TT hit rate)
// -----------------------------------------------------------------------

export interface PhaseEntry {
  phase: string;
  pieces: number;
  depth: number;
  nodes: number;
  timeMs: number;
  ttHitRate: number;
}

export const runPhaseComparison = async (
  onProgress: (entries: PhaseEntry[]) => void
): Promise<PhaseEntry[]> => {
  const positions = [
    { phase: 'Early game',  board: EARLY_POSITION },
    { phase: 'Mid game',    board: TEST_POSITION   },
    { phase: 'Late game',   board: LATE_POSITION   },
  ];

  const entries: PhaseEntry[] = [];

  for (const { phase, board } of positions) {
    const pieces = board.flat().filter(c => c !== Player.NONE).length;
    for (let d = 1; d <= 6; d++) {
      await tick();
      const t = performance.now();
      const { nodesEvaluated, ttHitRate } = getBestMove(board, d);
      entries.push({
        phase, pieces, depth: d,
        nodes: nodesEvaluated,
        timeMs: Math.round(performance.now() - t),
        ttHitRate,
      });
      onProgress([...entries]);
    }
  }

  return entries;
};

// -----------------------------------------------------------------------
// Experiment 4 — Fork detection impact
// -----------------------------------------------------------------------

export interface ForkResult {
  withForkWins: number;
  draws: number;
  noForkWins: number;
  games: number;
}

export const runForkComparison = async (
  numGames: number,
  onProgress: (r: ForkResult) => void
): Promise<ForkResult> => {
  const depth = 4; // depth 4 for both agents — fast enough for multiple games
  let withForkWins = 0, draws = 0, noForkWins = 0;

  for (let g = 0; g < numGames; g++) {
    await tick();
    // Alternate who goes first to remove first-move advantage bias
    const withForkIsYellow = g % 2 === 0;
    const outcome = withForkIsYellow
      ? simulateGame(depth, depth, false, true)   // red=noFork, yellow=withFork
      : simulateGame(depth, depth, true, false);  // red=withFork, yellow=noFork

    const withForkWon = withForkIsYellow ? outcome === 'yellow' : outcome === 'red';
    const noForkWon   = withForkIsYellow ? outcome === 'red'    : outcome === 'yellow';

    if (withForkWon) withForkWins++;
    else if (noForkWon) noForkWins++;
    else draws++;

    onProgress({ withForkWins, draws, noForkWins, games: g + 1 });
  }

  return { withForkWins, draws, noForkWins, games: numGames };
};
